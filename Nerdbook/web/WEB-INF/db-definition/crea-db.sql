CREATE DATABASE Nerdbook;

CREATE TABLE utenti (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	nome VARCHAR(256),
	cognome VARCHAR(256),
	username VARCHAR(256),
	password VARCHAR(256),
	urlFoto VARCHAR(256),
	frase VARCHAR(512),
	dataDiNascita DATE,
	gruppo INTEGER
	FOREIGN KEY(gruppo) REFERENCES gruppi(id)
);

CREATE TABLE gruppi (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	nome VARCHAR(256),
	urlfoto VARCHAR(256),
)

CREATE TABLE postType (
	id INTEGER  GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR(32)
)

CREATE TABLE posts (
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	autore INTEGER,
	bacheca_id INTEGER,
	gruppo BOOLEAN,
	testo VARCHAR(512),
	tipoAllegato INTEGER,
	allegato VARCHAR(512)
)